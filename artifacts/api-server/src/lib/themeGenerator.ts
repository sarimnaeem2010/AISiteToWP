const FUNCTIONS_PHP_TEMPLATE = (slug: string, blockNames: string[]) => `<?php
/**
 * ${slug} — generated by WP Bridge AI.
 * Enqueues the original template's CSS/JS and registers per-section blocks.
 */
if ( ! defined( 'ABSPATH' ) ) exit;

define( 'WPB_THEME_SLUG', '${slug}' );
define( 'WPB_THEME_DIR', get_stylesheet_directory() );
define( 'WPB_THEME_URI', get_stylesheet_directory_uri() );

add_action( 'wp_enqueue_scripts', function () {
    wp_enqueue_style( 'wpb-theme-style', WPB_THEME_URI . '/style.css', array(), '1.0.0' );
    wp_enqueue_style( 'wpb-template-css', WPB_THEME_URI . '/assets/template.css', array(), '1.0.0' );
    if ( file_exists( WPB_THEME_DIR . '/assets/template.js' ) ) {
        wp_enqueue_script( 'wpb-template-js', WPB_THEME_URI . '/assets/template.js', array( 'jquery' ), '1.0.0', true );
    }
}, 100 );

/**
 * Register every per-section block by reading its block.json + render.php.
 * Each block's render.php substitutes typed placeholders ({{TEXT:k}},
 * {{URL:k}}, {{ATTR:k}}) with the block's attribute values.
 */
add_action( 'init', function () {
    $blocks_dir = WPB_THEME_DIR . '/blocks';
    if ( ! is_dir( $blocks_dir ) ) return;
    foreach ( scandir( $blocks_dir ) as $entry ) {
        if ( $entry === '.' || $entry === '..' ) continue;
        $block_path = $blocks_dir . '/' . $entry;
        if ( is_dir( $block_path ) && file_exists( $block_path . '/block.json' ) ) {
            register_block_type( $block_path );
        }
    }
} );

/**
 * Render helper used by every block. Loads the template, performs typed
 * substitutions, and returns the resulting HTML.
 */
function wpb_render_section_template( string $template_path, array $attrs ): string {
    if ( ! file_exists( $template_path ) ) return '';
    $tpl = file_get_contents( $template_path );
    // First pass: rebase any {{THEME_URI}} placeholders to the actual child theme URL.
    // Field defaults that came from the original HTML's relative asset paths (images,
    // CSS backgrounds, etc.) also use this placeholder so they keep working when the
    // user edits and re-saves them.
    $theme_uri = esc_url( get_stylesheet_directory_uri() );
    $tpl = str_replace( '{{THEME_URI}}', $theme_uri, $tpl );
    foreach ( $attrs as $k => $v ) {
        if ( is_string( $v ) && strpos( $v, '{{THEME_URI}}' ) !== false ) {
            $attrs[ $k ] = str_replace( '{{THEME_URI}}', $theme_uri, $v );
        }
    }
    return preg_replace_callback(
        '/\\{\\{(TEXT|ATTR|URL):([A-Za-z0-9_]+)\\}\\}/',
        function ( $m ) use ( $attrs ) {
            $val = $attrs[ $m[2] ] ?? '';
            if ( ! is_string( $val ) ) $val = (string) $val;
            switch ( $m[1] ) {
                case 'TEXT': return esc_html( $val );
                case 'ATTR': return esc_attr( $val );
                case 'URL':  return esc_url( $val );
            }
            return '';
        },
        $tpl
    ) ?? '';
}

/**
 * Register Elementor widgets if Elementor is active. Each widget mirrors a
 * per-section block: same fields, same template substitution, so the user
 * can edit either inside Gutenberg or inside Elementor.
 */
// Optional Elementor wiring overrides — registers a category etc.
if ( file_exists( WPB_THEME_DIR . '/inc/elementor-widgets.php' ) ) {
    require_once WPB_THEME_DIR . '/inc/elementor-widgets.php';
}

add_action( 'elementor/widgets/register', function ( $widgets_manager ) {
    $widgets_dir = WPB_THEME_DIR . '/widgets';
    if ( ! is_dir( $widgets_dir ) ) return;
    require_once WPB_THEME_DIR . '/widgets/_base-widget.php';
    foreach ( glob( $widgets_dir . '/widget-*.php' ) as $widget_file ) {
        require_once $widget_file;
        // Generated widget files are named widget-<safeDir>.php and the class
        // inside is WPB_Widget_<safeDir-with-dashes-replaced-by-underscores>.
        // Strip the 'widget-' prefix FIRST, then normalise.
        $base  = preg_replace( '/^widget-/', '', basename( $widget_file, '.php' ) );
        $class = 'WPB_Widget_' . preg_replace( '/[^a-zA-Z0-9_]/', '_', $base );
        if ( class_exists( $class ) ) {
            $widgets_manager->register( new $class() );
        }
    }
} );
`;

const ELEMENTOR_BASE_WIDGET = `<?php
if ( ! defined( 'ABSPATH' ) ) exit;
if ( ! class_exists( 'WPB_Widget_Base' ) && class_exists( '\\\\Elementor\\\\Widget_Base' ) ) {
class WPB_Widget_Base extends \\Elementor\\Widget_Base {
    protected $wpb_id = '';
    protected $wpb_label = '';
    protected $wpb_template_path = '';
    protected $wpb_fields = array();

    public function get_name() { return 'wpb_' . $this->wpb_id; }
    public function get_title() { return $this->wpb_label; }
    public function get_icon() { return 'eicon-section'; }
    public function get_categories() { return array( 'general' ); }

    protected function register_controls() {
        $this->start_controls_section( 'content', array(
            'label' => esc_html__( 'Content', 'wpb' ),
            'tab'   => \\Elementor\\Controls_Manager::TAB_CONTENT,
        ) );
        foreach ( $this->wpb_fields as $f ) {
            $type = $f['type'] === 'text' && strlen( $f['default'] ?? '' ) > 80
                ? \\Elementor\\Controls_Manager::TEXTAREA
                : \\Elementor\\Controls_Manager::TEXT;
            $this->add_control( $f['key'], array(
                'label'   => esc_html( $f['label'] ?? $f['key'] ),
                'type'    => $type,
                'default' => $f['default'] ?? '',
            ) );
        }
        $this->end_controls_section();
    }

    protected function render() {
        $settings = $this->get_settings_for_display();
        echo wpb_render_section_template( WPB_THEME_DIR . '/' . $this->wpb_template_path, $settings );
    }
}
}
`;

function blockJsonFor(slug: string, section: ExtractedSection): string {
  const attributes: Record<string, { type: string; default: string }> = {};
  for (const f of section.fields) {
    attributes[f.key] = { type: "string", default: f.default };
  }
  return JSON.stringify(
    {
      $schema: "https://schemas.wp.org/trunk/block.json",
      apiVersion: 3,
      name: section.blockName,
      version: "1.0.0",
      title: section.label,
      category: "widgets",
      icon: "layout",
      description: `Pixel-perfect section from ${slug}`,
      keywords: [slug, section.category],
      supports: { html: false, align: ["wide", "full"] },
      attributes,
      render: "file:./render.php",
      textdomain: slug,
    },
    null,
    2,
  );
}

function blockRenderPhp(section: ExtractedSection, blockDirRel: string): string {
  return `<?php
/**
 * Render: ${section.label}
 * Auto-generated by WP Bridge AI. Do not hand-edit — regenerate the theme
 * if the source HTML structure changes.
 */
$attrs = isset( $attributes ) && is_array( $attributes ) ? $attributes : array();
echo wpb_render_section_template( __DIR__ . '/template.html', $attrs );
`;
}

function widgetPhpFor(section: ExtractedSection, templateRelPath: string): string {
  const safeId = section.blockName.split("/")[1].replace(/[^a-zA-Z0-9_]/g, "_");
  const fieldsJson = JSON.stringify(section.fields).replace(/'/g, "\\'");
  return `<?php
if ( ! defined( 'ABSPATH' ) ) exit;
if ( ! class_exists( 'WPB_Widget_Base' ) ) return;
class WPB_Widget_${safeId} extends WPB_Widget_Base {
    public function __construct( $data = array(), $args = null ) {
        $this->wpb_id = '${safeId}';
        $this->wpb_label = ${JSON.stringify(section.label)};
        $this->wpb_template_path = ${JSON.stringify(templateRelPath)};
        $this->wpb_fields = json_decode( '${fieldsJson}', true ) ?: array();
        parent::__construct( $data, $args );
    }
}
`;
}

const ASSET_EXT = new Set(["png", "jpg", "jpeg", "gif", "webp", "avif", "svg", "ico", "woff", "woff2", "ttf", "otf", "eot"]);

/**
 * Build a complete WordPress child theme ZIP. The ZIP lives under a single
 * top-level folder named `${projectSlug}` so it can be uploaded directly
 * via Appearance → Themes → Add New → Upload.
 */
export function generateThemeZip(input: ThemeInput): Buffer {
  const { projectSlug, projectName, combinedCss, combinedJs, pages, sourceZip } = input;
  const zip = new AdmZip();
  const root = projectSlug;

  zip.addFile(`${root}/style.css`, Buffer.from(STYLE_HEADER(projectName, projectSlug) + "\n" + combinedCss, "utf8"));
  zip.addFile(`${root}/index.php`, Buffer.from(INDEX_PHP, "utf8"));
  zip.addFile(`${root}/page.php`, Buffer.from(PAGE_PHP, "utf8"));
  zip.addFile(`${root}/header.php`, Buffer.from(HEADER_PHP, "utf8"));
  zip.addFile(`${root}/footer.php`, Buffer.from(FOOTER_PHP, "utf8"));
  zip.addFile(`${root}/inc/elementor-widgets.php`, Buffer.from(ELEMENTOR_WIDGETS_INC, "utf8"));
  zip.addFile(`${root}/assets/template.css`, Buffer.from(combinedCss, "utf8"));
  if (combinedJs.trim().length > 0) {
    zip.addFile(`${root}/assets/template.js`, Buffer.from(combinedJs, "utf8"));
  }
  zip.addFile(`${root}/widgets/_base-widget.php`, Buffer.from(ELEMENTOR_BASE_WIDGET, "utf8"));

  const allBlockNames: string[] = [];
  const seen = new Set<string>();
  for (const page of pages) {
    for (const section of page.sections) {
      if (seen.has(section.blockName)) continue;
      seen.add(section.blockName);
      allBlockNames.push(section.blockName);
      const safeDir = section.blockName.split("/")[1];
      const blockRel = `blocks/${safeDir}`;
      zip.addFile(`${root}/${blockRel}/block.json`, Buffer.from(blockJsonFor(projectSlug, section), "utf8"));
      zip.addFile(`${root}/${blockRel}/render.php`, Buffer.from(blockRenderPhp(section, blockRel), "utf8"));
      zip.addFile(`${root}/${blockRel}/template.html`, Buffer.from(section.template, "utf8"));
      const widgetRelTpl = `${blockRel}/template.html`;
      zip.addFile(`${root}/widgets/widget-${safeDir}.php`, Buffer.from(widgetPhpFor(section, widgetRelTpl), "utf8"));
    }
  }

  zip.addFile(`${root}/functions.php`, Buffer.from(FUNCTIONS_PHP_TEMPLATE(projectSlug, allBlockNames), "utf8"));

  // Copy original images + fonts into assets/ so paths resolve identically.
  if (sourceZip) {
    try {
      const src = new AdmZip(sourceZip);
      for (const e of src.getEntries()) {
        if (e.isDirectory) continue;
        const name = e.entryName.replace(/\\/g, "/");
        if (name.startsWith("__MACOSX/") || name.includes("/.DS_Store")) continue;
        const ext = name.split(".").pop()?.toLowerCase() ?? "";
        if (!ASSET_EXT.has(ext)) continue;
        zip.addFile(`${root}/assets/${name}`, e.getData());
      }
    } catch {
      // Original ZIP missing or corrupt — skip the asset bundle (the theme
      // still renders, just with broken local image refs).
    }
  }

  const readme = `# ${projectName} (Pixel-Perfect Theme)

Generated by WP Bridge AI. Upload via WordPress → Appearance → Themes → Add New → Upload Theme. Activate.

Each section of the original site is registered as both a Gutenberg block and an Elementor widget. Edit content in either editor — the original CSS/JS keeps the design pixel-perfect.
`;
  zip.addFile(`${root}/README.md`, Buffer.from(readme, "utf8"));

  return zip.toBuffer();
}
